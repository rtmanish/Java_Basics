package If_else;

import java.util.Scanner;

public class Int_to_String {

	public static void main(String[] args) {
		Scanner s= new Scanner(System.in);
		int n=s.nextInt();
		if(n==1) {
			System.out.println("One");
		}
		if(n==2) {
			System.out.println("Two");
		}
		if(n==3) {
			System.out.println("Three");
		}
	}

}
