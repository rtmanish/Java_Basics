package arrays;

// An array is basically collection of fixed number of elements of a single type


public class arrayUse {

	public static void main(String[] args) {
		int arr[]=new int[10]; // int tells the type of array, [] denotes that it is an array, 'new' keyword will allocate memory for us , 10 tells size of an array 
		arr[4]=20;
		System.out.println(arr[4]);
	}

}


